#define SPRITE_WIDTH 17
#define SPRITE_HEIGHT 19

uint16_t x = 2;
uint16_t y = 2;

int8_t incX=1;
int8_t incY=0;
uint8_t *sprite = right;

/**
 *
 */
void moveSprite(){
  x += incX;
  y += incY;

  if (x > (LCD_WIDTH-SPRITE_WIDTH-3)){
    x--;
    incX=0;
    incY=1;
    sprite = down;
  }
  if (y > (LCD_HEIGHT-SPRITE_HEIGHT-3)){
    y--;
    incX=-1;
    incY=0;
    sprite = left;
  }
  if (x < 3){
    x++;
    incX=0;
    incY=-1;
    sprite = up;
  }
  if (y < 3){
    y++;
    incX=1;
    incY=0;
    sprite = right;
  }
}
